    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        try {
            // TODO add your handling code here:
            String prescriptionID = phid.getText();
            con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","root");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("Select * from prescription where phid = '"+prescriptionID+"' ");
            if(rs.next())
            {
                phid.setText(rs.getString(1));
                dtype.setText(rs.getString(2));
                drugname.setText(rs.getString(3));
                drugstrength.setText(rs.getString(4));
                drugdose.setText(rs.getString(5));
                drugduration.setText(rs.getString(6));
                dadvice.setText(rs.getString(7));
                co.setText(rs.getString(8));
                oe.setText(rs.getString(9));
                jDateChooser1.setDate(rs.getDate(10));
                jTextField2.setText(rs.getString(11));
            }
            else
                JOptionPane.showMessageDialog(null,"Please enter correct Prescription ID");
        } catch (SQLException ex) {
            Logger.getLogger(Prescriptionmain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchbtnActionPerformed

