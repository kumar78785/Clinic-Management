    public void Phid()
    {
        
        try {
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("select MAX(phid) from prescription");
            rs.next();
            
            
            rs.getString("MAX(phid)");
            if(rs.getString("MAX(phid)") == null)
            {
                phidno.setText("PH-001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("MAX(phid)").substring(3,rs.getString("MAX(phid)").length()));
                id++;
                phidno.setText("PH-"+String.format("%03d",id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prescription.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }

    public void Drugname()
    {
        try {
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("select tname from drug");
            while(rs.next())
            {
                String name = rs.getString("tname");
                jComboBox1.addItem(name);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Prescriptionmain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
