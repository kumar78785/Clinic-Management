    public void Did()
    {
        try {
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("select MAX(did) from drug");
            rs.next();
            
            
            rs.getString("MAX(did)");
            if(rs.getString("MAX(did)") == null)
            {
                did.setText("D-001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("MAX(did)").substring(2,rs.getString("MAX(did)").length()));
                id++;
                did.setText("D-"+String.format("%03d",id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Drugmain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
